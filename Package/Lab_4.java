package LAB_Report.Package;
/*
Write a program in Java to create a java package.
 */
public class Lab_4 {
    public static void main(String[] args) {
        // I have created package Lab_4 within the package LAB_Report
        // and to declare the package
        // I have written
        // package LAB_Report.Package;
        // this means this class is a part of the package LAB_Report.Package
        // we can create many packages within the same package
        // we should declare the package with as
        // package packageName;             // this is for only one package
        // package packageName.packageName; // this is for multiple package within the same package
        // creating packages help to organize the code
        System.out.println("Package is created successfully");

    }
}
